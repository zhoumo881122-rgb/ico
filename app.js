function pickLanguage(value = "auto") {
  if (value !== "auto") return translations[value] ? value : "en";
  const nav = navigator.language || "en";
  if (nav.toLowerCase().startsWith("zh")) return "zh-CN";
  const short = nav.slice(0, 2);
  return translations[short] ? short : "en";
}

function applyLanguage(value) {
  const lang = pickLanguage(value);
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = translations[lang][key] || translations.en[key] || node.textContent;
  });
  localStorage.setItem("faviconLang", value);
}

function setupLanguage() {
  const params = new URLSearchParams(location.search);
  const requested = params.get("lang") || localStorage.getItem("faviconLang") || "auto";
  languageSelect.value = translations[requested] || requested === "auto" ? requested : "auto";
  applyLanguage(languageSelect.value);
  languageSelect.addEventListener("change", () => applyLanguage(languageSelect.value));
}

function drawPlaceholder() {
  ctx.clearRect(0, 0, canvas.width, canvas.height);
  ctx.fillStyle = "#eef2ee";
  ctx.fillRect(0, 0, canvas.width, canvas.height);
  ctx.fillStyle = "#8aa19a";
  ctx.font = "700 22px system-ui";
  ctx.textAlign = "center";
  ctx.fillText("Upload image", canvas.width / 2, canvas.height / 2);
}

function roundedClip(context, size, radiusPercent) {
  const radius = Math.min(size / 2, (size * radiusPercent) / 100);
  context.beginPath();
  context.moveTo(radius, 0);
  context.lineTo(size - radius, 0);
  context.quadraticCurveTo(size, 0, size, radius);
  context.lineTo(size, size - radius);
  context.quadraticCurveTo(size, size, size - radius, size);
  context.lineTo(radius, size);
  context.quadraticCurveTo(0, size, 0, size - radius);
  context.lineTo(0, radius);
  context.quadraticCurveTo(0, 0, radius, 0);
  context.closePath();
}

function renderToCanvas(size) {
  const output = document.createElement("canvas");
  output.width = size;
  output.height = size;
  const outputCtx = output.getContext("2d");
  outputCtx.clearRect(0, 0, size, size);
  outputCtx.save();
  roundedClip(outputCtx, size, Number(radiusRange.value));
  outputCtx.clip();
  if (!transparentBg.checked) {
    outputCtx.fillStyle = bgColor.value;
    outputCtx.fillRect(0, 0, size, size);
  }
  if (state.image) {
    const scale = Math.max(size / state.image.width, size / state.image.height) * state.zoom;
    const width = state.image.width * scale;
    const height = state.image.height * scale;
    const x = (size - width) / 2 + state.offsetX * (size / canvas.width);
    const y = (size - height) / 2 + state.offsetY * (size / canvas.height);
    outputCtx.imageSmoothingQuality = "high";
    outputCtx.drawImage(state.image, x, y, width, height);
  }
  outputCtx.restore();
  return output;
}

function redraw() {
  if (!state.image) {
    drawPlaceholder();
    return;
  }
  const previewCanvas = renderToCanvas(canvas.width);
  ctx.clearRect(0, 0, canvas.width, canvas.height);
  ctx.drawImage(previewCanvas, 0, 0);
  updatePreviews();
  updateSnippet();
}

function selectedSizes() {
  return [...document.querySelectorAll(".size-grid input:checked")]
    .map((input) => Number(input.value))
    .sort((a, b) => a - b);
}

function updatePreviews() {
  previewStrip.innerHTML = "";
  const sizes = [16, 32, 48, 180, 192, 512];
  sizes.forEach((size) => {
    const item = document.createElement("div");
    item.className = "preview-item";
    const img = document.createElement("img");
    img.alt = `${size}x${size} preview`;
    img.src = renderToCanvas(size).toDataURL("image/png");
    const label = document.createElement("span");
    label.textContent = `${size}px`;
    item.append(img, label);
    previewStrip.append(item);
  });
}

function updateSnippet() {
  htmlSnippet.textContent = `<link rel="icon" href="/favicon.ico" sizes="any">\n<link rel="icon" type="image/png" sizes="32x32" href="/favicon-32x32.png">\n<link rel="apple-touch-icon" sizes="180x180" href="/apple-touch-icon.png">\n<link rel="manifest" href="/site.webmanifest">`;
}

function setEnabled(enabled) {
  zoomRange.disabled = !enabled;
  downloadIco.disabled = !enabled;
  downloadPngs.disabled = !enabled;
  copyHtml.disabled = !enabled;
}

function loadFile(file) {
  if (!file || !file.type.startsWith("image/")) return;
  state.imageName = file.name.replace(/\.[^.]+$/, "") || "favicon";
  const reader = new FileReader();
  reader.onload = () => {
    const img = new Image();
    img.onload = () => {
      state.image = img;
      state.zoom = 1;
      state.offsetX = 0;
      state.offsetY = 0;
      zoomRange.value = "1";
      setEnabled(true);
      redraw();
    };
    img.src = reader.result;
  };
  reader.readAsDataURL(file);
}

function canvasToBlob(sourceCanvas) {
  return new Promise((resolve) => sourceCanvas.toBlob(resolve, "image/png"));
}

async function buildIco() {
  const icoSizes = selectedSizes().filter((size) => size <= 256);
  const sizes = icoSizes.length ? icoSizes : [16, 32, 48];
  const images = await Promise.all(
    sizes.map(async (size) => ({
      size,
      bytes: new Uint8Array(await (await canvasToBlob(renderToCanvas(size))).arrayBuffer())
    }))
  );
  const headerSize = 6 + images.length * 16;
  const totalSize = headerSize + images.reduce((sum, image) => sum + image.bytes.length, 0);
  const buffer = new ArrayBuffer(totalSize);
  const view = new DataView(buffer);
  view.setUint16(0, 0, true);
  view.setUint16(2, 1, true);
  view.setUint16(4, images.length, true);
  let offset = headerSize;
  images.forEach((image, index) => {
    const entry = 6 + index * 16;
    view.setUint8(entry, image.size >= 256 ? 0 : image.size);
    view.setUint8(entry + 1, image.size >= 256 ? 0 : image.size);
    view.setUint8(entry + 2, 0);
    view.setUint8(entry + 3, 0);
    view.setUint16(entry + 4, 1, true);
    view.setUint16(entry + 6, 32, true);
    view.setUint32(entry + 8, image.bytes.length, true);
    view.setUint32(entry + 12, offset, true);
    new Uint8Array(buffer, offset, image.bytes.length).set(image.bytes);
    offset += image.bytes.length;
  });
  return new Blob([buffer], { type: "image/x-icon" });
}

function crc32(bytes) {
  let crc = -1;
  for (const byte of bytes) {
    crc ^= byte;
    for (let bit = 0; bit < 8; bit += 1) {
      crc = (crc >>> 1) ^ (0xedb88320 & -(crc & 1));
    }
  }
  return (crc ^ -1) >>> 0;
}

function dosTime(date = new Date()) {
  return (
    ((date.getHours() & 0x1f) << 11) |
    ((date.getMinutes() & 0x3f) << 5) |
    (Math.floor(date.getSeconds() / 2) & 0x1f)
  );
}

function dosDate(date = new Date()) {
  return (
    (((date.getFullYear() - 1980) & 0x7f) << 9) |
    (((date.getMonth() + 1) & 0x0f) << 5) |
    (date.getDate() & 0x1f)
  );
}

async function buildZip(files) {
  const encoder = new TextEncoder();
  const localParts = [];
  const centralParts = [];
  let offset = 0;
  const now = new Date();

  files.forEach((file) => {
    const nameBytes = encoder.encode(file.name);
    const data = file.bytes;
    const crc = crc32(data);

    const local = new ArrayBuffer(30 + nameBytes.length);
    const localView = new DataView(local);
    localView.setUint32(0, 0x04034b50, true);
    localView.setUint16(4, 20, true);
    localView.setUint16(6, 0, true);
    localView.setUint16(8, 0, true);
    localView.setUint16(10, dosTime(now), true);
    localView.setUint16(12, dosDate(now), true);
    localView.setUint32(14, crc, true);
    localView.setUint32(18, data.length, true);
    localView.setUint32(22, data.length, true);
    localView.setUint16(26, nameBytes.length, true);
    localView.setUint16(28, 0, true);
    new Uint8Array(local, 30).set(nameBytes);
    localParts.push(local, data);

    const central = new ArrayBuffer(46 + nameBytes.length);
    const centralView = new DataView(central);
    centralView.setUint32(0, 0x02014b50, true);
    centralView.setUint16(4, 20, true);
    centralView.setUint16(6, 20, true);
    centralView.setUint16(8, 0, true);
    centralView.setUint16(10, 0, true);
    centralView.setUint16(12, dosTime(now), true);
    centralView.setUint16(14, dosDate(now), true);
    centralView.setUint32(16, crc, true);
    centralView.setUint32(20, data.length, true);
    centralView.setUint32(24, data.length, true);
    centralView.setUint16(28, nameBytes.length, true);
    centralView.setUint16(30, 0, true);
    centralView.setUint16(32, 0, true);
    centralView.setUint16(34, 0, true);
    centralView.setUint16(36, 0, true);
    centralView.setUint32(38, 0, true);
    centralView.setUint32(42, offset, true);
    new Uint8Array(central, 46).set(nameBytes);
    centralParts.push(central);

    offset += local.byteLength + data.length;
  });

  const centralSize = centralParts.reduce((sum, part) => sum + part.byteLength, 0);
  const end = new ArrayBuffer(22);
  const endView = new DataView(end);
  endView.setUint32(0, 0x06054b50, true);
  endView.setUint16(8, files.length, true);
  endView.setUint16(10, files.length, true);
  endView.setUint32(12, centralSize, true);
  endView.setUint32(16, offset, true);

  return new Blob([...localParts, ...centralParts, end], { type: "application/zip" });
}

function downloadBlob(blob, filename) {
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = filename;
  document.body.append(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

async function downloadSelectedPngs() {
  const files = [];
  for (const size of selectedSizes()) {
    const blob = await canvasToBlob(renderToCanvas(size));
    const name = size === 180 ? "apple-touch-icon.png" : `favicon-${size}x${size}.png`;
    files.push({ name, bytes: new Uint8Array(await blob.arrayBuffer()) });
  }
  const manifest = {
    name: "Website",
    short_name: "Website",
    icons: [
      { src: "/favicon-192x192.png", sizes: "192x192", type: "image/png" },
      { src: "/favicon-512x512.png", sizes: "512x512", type: "image/png" }
    ],
    theme_color: "#ffffff",
    background_color: "#ffffff",
    display: "standalone"
  };
  files.push({ name: "site.webmanifest", bytes: new TextEncoder().encode(JSON.stringify(manifest, null, 2)) });
  files.push({ name: "favicon-html-tags.txt", bytes: new TextEncoder().encode(htmlSnippet.textContent) });
  downloadBlob(await buildZip(files), "favicon-assets.zip");
}

function setupEvents() {
  chooseFile.addEventListener("click", (event) => {
    event.stopPropagation();
    fileInput.click();
  });
  dropZone.addEventListener("click", () => fileInput.click());
  dropZone.addEventListener("keydown", (event) => {
    if (event.key === "Enter" || event.key === " ") fileInput.click();
  });
  fileInput.addEventListener("change", () => loadFile(fileInput.files[0]));
  ["dragenter", "dragover"].forEach((name) => {
    dropZone.addEventListener(name, (event) => {
      event.preventDefault();
      dropZone.classList.add("is-over");
    });
  });
  ["dragleave", "drop"].forEach((name) => {
    dropZone.addEventListener(name, () => dropZone.classList.remove("is-over"));
  });
  dropZone.addEventListener("drop", (event) => {
    event.preventDefault();
    loadFile(event.dataTransfer.files[0]);
  });
  zoomRange.addEventListener("input", () => {
    state.zoom = Number(zoomRange.value);
    redraw();
  });
  [radiusRange, bgColor, transparentBg].forEach((input) => input.addEventListener("input", redraw));
  document.querySelectorAll(".size-grid input").forEach((input) => input.addEventListener("change", updateSnippet));
  canvas.addEventListener("pointerdown", (event) => {
    if (!state.image) return;
    canvas.setPointerCapture(event.pointerId);
    state.dragging = true;
    state.pointerStart = { x: event.clientX, y: event.clientY, offsetX: state.offsetX, offsetY: state.offsetY };
  });
  canvas.addEventListener("pointermove", (event) => {
    if (!state.dragging || !state.pointerStart) return;
    state.offsetX = state.pointerStart.offsetX + event.clientX - state.pointerStart.x;
    state.offsetY = state.pointerStart.offsetY + event.clientY - state.pointerStart.y;
    redraw();
  });
  canvas.addEventListener("pointerup", () => {
    state.dragging = false;
    state.pointerStart = null;
  });
  downloadIco.addEventListener("click", async () => downloadBlob(await buildIco(), "favicon.ico"));
  downloadPngs.addEventListener("click", downloadSelectedPngs);
  copyHtml.addEventListener("click", async () => {
    await navigator.clipboard.writeText(htmlSnippet.textContent);
    const original = copyHtml.textContent;
    copyHtml.textContent = "Copied";
    setTimeout(() => {
      copyHtml.textContent = original;
    }, 1200);
  });
}

